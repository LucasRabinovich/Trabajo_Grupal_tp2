const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    rol: {
        type: String,
        enum: ['Admin', 'Usuario'],
        default: 'Usuario'
    }
});

module.exports = mongoose.model('User', userSchema);